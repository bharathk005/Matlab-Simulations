a1=1;
a2=2;
a3=3;
a4=4;
f1=100;
f2=200;
f3=300;
f4=400;
fs=200;
t=1/fs;
n=1:400;
x1=a1*sin(2*pi*n*f1*t);
x2=a2*sin(2*pi*n*f2*t);
x3=a3*sin(2*pi*n*f3*t);
x4=a4*sin(2*pi*n*f4*t);
x= [ zeros(1,400) x1 x2 x3 x4 zeros(1,400)];
nx = 1200;
subplot(5,1,1);
plot(x,nx);
y1=x1/a1;
y2=x2/a2;
y3=x3/a3;
y4=x4/a4;
for i=1:1:2000;
y5= [ zeros(1,i) y1 zeros(1,2000-i)];
a=x.*y5;
z1(i)=sum(a);
y6= [ zeros(1,i) y2 zeros(1,2000-i)];
b=x.*y6;
z2(i)=sum(b);
y7= [ zeros(1,i) y3 zeros(1,2000-i)];
c=x.*y7;
z3(i)=sum(c);
y8= [ zeros(1,i) y4 zeros(1,2000-i)];
d=x.*y8;
z4(i)=sum(d);
end;
subplot(5,1,2);
plot(z1,n);
subplot(5,1,3);
plot(z2,n);
subplot(5,1,4);
plot(z3,n);
subplot(5,1,5);
plot(z4,n);